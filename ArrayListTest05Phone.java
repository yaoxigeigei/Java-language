public class ArrayListTest05Phone {
    private String brand;
    private int price;

    public ArrayListTest05Phone() {
    }

    public ArrayListTest05Phone(String brand, int price) {
        this.brand = brand;
        this.price = price;
    }

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
}
